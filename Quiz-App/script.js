var index = -1;
var score = 0;

function loadques() {
  if (index + 1 < quiz.length) {
    index += 1;
    curr = quiz[index];
    document.getElementById('question').innerText = curr.ques;
    document.getElementById('option1').innerText = curr.opt1;
    document.getElementById('option2').innerText = curr.opt2;
    document.getElementById('option3').innerText = curr.opt3;
    document.getElementById('option4').innerText = curr.opt4;
  } else {
    document.getElementById('question').innerText = `You have scored ${score} out of ${quiz.length}`;
    document.getElementById('opt-grp').style.display = 'none';
    document.getElementById('next').style.display = 'none';
    document.getElementById('restart').style.display = 'inline';
  }
}
loadques();

document.getElementById('next').addEventListener('click', () => {
  var select = document.querySelector('input[name=opt]:checked');
  if (select === null)
    alert('Select an option!');
  else if (select.id === quiz[index].correct) {
    score++;
    select.checked = false;
    loadques();
  } else {
    select.checked = false;
    loadques();
  }
})

document.getElementById('restart').addEventListener('click', () => {
  location.reload();
})
